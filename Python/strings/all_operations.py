# create string:
s = 'hello, good morning'

# raw string:
s = r'C:\path'

# repetition:
print('hello'* 3)

# index
s1 = 'hello good evening'
print(s1[0])

# slicing:
print(s1[:5])

# reverse:
print(s1[::-1])

# upper()
print(s1.upper())

# lower()
print(s1.lower())

# capitalize()
print(s1.capitalize())

# titlecase()
print(s1.title())

# swapcase()
print(s1.swapcase())

# strip whitespace
print(s1.strip())

# left strip()
print(s1.lstrip())

# right strip()
print(s1.rstrip())

# replace()
print(s1.replace('good', 'bad'))

# split()
print(s1.split())

# join
print(', '.join(['a', 'b', 'c']))

# startswith
print(s1.startswith('hello'))

# endswith
print(s1.endswith('evening'))

# find substring
print(s1.find('good'))

# find count
print(s1.count('o'))

s2 = 'hi the password is winter@#$%^.gmail.com'
print(s2.isalnum())
print(s2.isalpha())
print(s2.isdigit())
print(s2.islower())
print(s2.isupper())
print(s2.istitle())
print(s2.isspace())
print(s2.center(20, '*'))










































































